package com.company;

public class funcoesBooleanas {
    static boolean conjuncoes(boolean valorUm,boolean valorDois){
        if(valorUm && valorDois)
            return true;
          else return false;
    }

    boolean disjuncoes(boolean valorUm,boolean valorDois){
        if(valorUm || valorDois)
            return true;
        else return false;

    }
}
